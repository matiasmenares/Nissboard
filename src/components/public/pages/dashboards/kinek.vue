  export default {
    name: 'Kinek',
    data(){
        return{
            sheet: false,
            ecu: {rpm: 0, speed: 0, temp: 0, batt: 0, turbo: 0.0, tps: 0, intake: 0, timming: 0},
            analog: {turbo: { psi: {value: "0.0", raw: "0000", peak: "0.0"}, bar: {value: "0.0", raw: "0000", peak: "0.0"}}},
            channel_output: [],
            slots: [],
            dash_outputs: [],
            color: {rpm: "light-blue"},
            value: 0,
            red_line: 6000,
            yellow_line: 5000
        }
    },
    components: {
    },
    beforeMount(){
        this.set_dash_output()
    },
    created() {
        // this.set_data()
        this.set_channel_output()
    },
    methods: {
        set_data(){
            this.sockets.subscribe('ecuData', (data) => {
                this.ecu = data;
            })
        },
        set_channel_output(){
            this.sockets.subscribe('channelOutput', (data) => {
                let response = []
                data.map(result => {
                    this.slots.map(slot => {
                        if(slot.channel_output_id == result.id){
                            response[slot.id] = result
                        }
                    })
                })
                this.channel_output = response
            })
        },
        set_dash_output(){
            this.axios.get("dashboards").then(result => {
                this.slots = result.data.dashboard_outputs.filter(slot => slot.dashboard_id == 1 )
                this.red_line = parseInt(result.data["kinkek"][4])
                this.yellow_line = parseInt(result.data["kinkek"][5])
            }).catch(error => {
                console.log(error);
            })
        },
        set_rpm_color(value){
            if (this.red_line > 0){
                if(value > this.red_line){
                    //danger
                    return "red darken-2"
                }else if(this.yellow_line > 0){
                    if(value > this.yellow_line && value < this.red_line){
                        //warning
                        return "amber"
                    }else{
                        //success
                        return "light-blue" 
                    }
                }else{
                    //success
                    return "light-blue" 
                }
            }else if(this.yellow_line > 0){
                if(value > this.red_line){
                    //warning
                    return"amber"
                }
            }else{
                //success
                return "light-blue"
            }            
        }
    },
    computed:{
        rpm_bar(){
            if(this.channel_output[1]){
                return (this.channel_output[1]['value'] * 100) / 8000
            }else{
                return 0
            }
        },
        rpm_color(){
            if(this.channel_output[1]){
                return this.set_rpm_color(this.channel_output[1]['value'])
            }else{
                return "light-blue"
            }
        }
    },
    watch: {
        
    }
  }
</script>

